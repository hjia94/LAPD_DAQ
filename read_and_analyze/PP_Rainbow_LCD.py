cm_data2[BP[3]:BP[4],1] = 1
cm_data2[BP[3]:BP[4],2] = 0

cm_data2[BP[4]:BP[5],0] = 1
x = numpy.linspace(1, 0, BP[5]-BP[4], endpoint=False)
y = (math.cos(0.2*math.pi) - numpy.cos(0.2*math.pi+x*math.pi*0.8)) / (math.cos(0.2*math.pi)+1)
cm_data2[BP[4]:BP[5],1] = y
cm_data2[BP[4]:BP[5],2] = 0


