"""
PP colormap. On many monitors this gives more graduation within each color, in comparison to the original.  See test at end.

This file was originally based on 034_Rainbow.py from https://github.com/planetarymike/IDL-Colorbars  -- see the IDL_py_test subdirectory

PP downloaded the original file on 2019-07-21,  but this is HIGHLY edited

The colors start with R,G,B = 0.5, 0.0, 1.0
and vary smoothly between breakpoints in list BP below.
PP arrived at the functional variation between BPs by trial and error

 segment         Red        Green        Blue
 -------       -------     -------      ------
0   to BP1:   0.5 to 0        0            1
BP1 to BP2:       0         0 to 1         1
BP2 to BP3:       0           1          1 to 0
BP3 to BP4:     0 to 1        1            0
BP4 to 256:       1         1 to 0         0

Note: the default matplotlib color cycle is:
'tab:blue'
'tab:orange'
'tab:green'
'tab:red'
'tab:purple'
'tab:brown'
'tab:pink'
'tab:gray'
'tab:olive'
'tab:cyan'

there are 3 color tables here,
	cm_data0 = original
	cm_data1 = PP initial attempt at modifying cm_data0
	cm_data2 = PP final

the final result is generated using cmap = LinearSegmentedColormap(cm_data2)

"""

